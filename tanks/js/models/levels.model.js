export class LevelsModel {
  constructor() {
    this.arr = getLevelMaps();
  }

  getMap(level) {
    return this.arr[level];
  }

  getMapsAmount() {
    const mapsAmount = Object.keys(getLevelMaps()).length;
    return mapsAmount;
  }

  getMapWidth(level) {
    const map = this.getMap(level);
    return map[0].length;
  }

  getMapHeight(level) {
    const map = this.getMap(level);
    return map.length;
  }

}


