import { maps } from './level-maps.js';
import { enemies } from './level-enemies.js';

export class LevelsModel {
  constructor() {
    this.mapsObj = maps;
    this.enemiesObj = enemies;
  }

  getMap(level) {
    return this.mapsObj[level];
  }

  getEnemies(level) {
    return this.enemiesObj[level];
  }

  getMapsAmount() {
    const mapsAmount = Object.keys(this.mapsObj).length;
    return mapsAmount;
  }

  getMapWidth(level) {
    const map = this.getMap(level);
    return map[0].length;
  }

  getMapHeight(level) {
    const map = this.getMap(level);
    return map.length;
  }

}


